/**
 * @param {number[][]} towers
 * @param {number[]} center
 * @param {number} radius
 * @return {number[]}
 */
var bestTower = function(towers, center, radius) {
    const [x1, y1] = center;

    let res = [-1, -1];
    let maxQ = -Infinity;

    for (let i = 0; i < towers.length; i++) {
        const [x2,y2,q] = towers[i];
        const canReach = Math.abs(x1 - x2) + Math.abs(y1 - y2) <= radius;

        if (canReach && q > maxQ) {
            maxQ = q;
            res[0] = x2;
            res[1] = y2;
        } else if (canReach && q === maxQ) {
            const isResSmallerCurrentByLexical = res[0] < x2 || (x2 === res[0] && res[1] < y2);

            if (!isResSmallerCurrentByLexical) {
                maxQ = q;
                res[0] = x2;
                res[1] = y2;
            }
        }

    }

    return res;
};


